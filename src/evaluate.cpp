void Board_State::increment_evalPos(Square sq , Piece piece , bool add , bool endgame ){
    if(add){
    eval[piece.get_piece_color()] += pieceEval[ endgame ? 1:0][piece.pieceType][sq.squareNo];
    }
    else{
         eval[piece.get_piece_color()]-= pieceEval[ endgame ? 1:0][piece.pieceType][sq.squareNo];
    }
    
}

int evalPos(Board_State board){
    if(board.side==WHITE){ 
        return board.eval[0]-board.eval[1];
    }
    else{
        return board.eval[1]-board.eval[0];
    }
}